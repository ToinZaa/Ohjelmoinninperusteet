Num1 = 47
# Muuttujat Num1 ja Num2 saavat kokonaislukuarvot
Num2 = 102
Sum = Num1 + Num2
# Lasketaan summa ja erotus
Diff = Num2 - Num1
Product = Sum * Diff
# Lasketaan laskutoimitusten tulokset
print(Num1, "+", Num2, "=", Sum)
print(Num2, "-", Num1, "=", Diff)
print(Sum, "*", Diff, "=", Product)
print("(",Num1,"+",Num2,")*(",Num2,"-",Num1,")=",Product)

